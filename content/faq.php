<h3>Čo je funduj.sk?</h3>
  <div>
    <p>
		Určite ste už aj vy s kamáratmi pri pive rozoberali rôzne Vaše nápady a projekty. Od realizácie Vás nakoniec odradili financie, ktoré ste nemali. 
		ASDF je platforma, ktorá umožňuje kreatívnym ľuďom realizovať a hlavne financovať svoje nápady prostredníctvom crowdfundingu.
    </p>
  </div>
  <h3>Čo je to crowdfunding?</h3>
  <div>
    <p>
	Ako vraví všemúdra <a href="http://sk.wikipedia.org/wiki/Crowdfunding">	wikipedia</a>, crowdfunding je spôsob financovania projektov "davom".
		ADSAFFSS poskytuje priestor na prezentáciu zaujímavých projektov za účelom získania pomoci (nie len finančnej) od „davu“. Dá sa povedať, že 
		ASDF predstavuje premostenie medzi ľuďmi – podporovateľmi a autormi projektov.
    </p>
  </div>
  <h3>Aké projekty máte na mysli?</h3>
  <div>
    <p>
    Sme si vedomí, že na Slovensku je podpora kohokoľvek od inštitúcií na to určených chabá, avšak ASDF poskytuje priestor len pre kreatívne nápady ako napríklad 
		natočenie dokumentu, nahradnie hudobného albumu, oživenie verejného prietoru niečím zaujímavým a podobne. To znamená, že cez tento portál nie je možné 
		realizovať charitatívne zbierky. 
    </p>
  </div>
  <h3>Prečo je dobré prispieť?</h3>
  <div>
    <p>
   		Predstavte si situáciu keď Vaša obľúbená kapela vydáva nový album. Album nájdete na internete týždeň pred oficiálnym dátumom vydania. Povedzme, že si ho stiahnete. Samozrejme, je skvelý, ale lenivosť Vás zastaví pred jeho kúpou. Teraz si predstavte situáciu, že tá istá kapela nemá na nahranie a vydanie nového albumu peniaze, a tak Vás osloví, aby ste jej prispeli nejaké drobné. Prispejete sumu, ktorú by ste zaplatili aj za samotný album v obchode a dostanete navyše tričko a voľný vstup na ľubovoľný koncert. 
		Dôležité je ale niečo úplne iné. Tým, že prispejete, sa stávate prakticky co-producerom albumu. A pocit, že bez Vás by toto dielo nikdy nevzniklo si z internetu nestiahnete , ba nekúpite si ho ani v hudobninách.
    </p>
  </div>
  <h3>Prečo je dobré založiť projekt?</h3>
  <div>
    <p>
   		Toto ani nie je potrebné vysvetlovať. Ak sa Vám desiatky alebo stovky ľudí vyzbierajú na Váš projekt, ktorý nosíte v hlave roky. Je to pocit ale hlavne motivácia, aká sa hľadá ťažko.
    </p>
  </div>
  <h3>Ako založiť projekt?</h3>
  <div>
    <p>
   		Je to veľmi jednoduché. Ak máš už 18 rokov, stačí sa zaregistrovať a v hornej časti stránky kliknúť na "Nový projekt". Tipy a triky k zakladniu projektov nájdete aj v tu.
    </p>
  </div>
	 <h3>Ako prispieť?</h3>
  <div>
    <p>
  	 	Nakoľko sa snažíme držať náklady na celý projet pri zemi, prispievať na projekty je možné zatiaľ iba bakovým prevodom alebo cez PayPal. Transakčné poplatky v prípade prevodu sú vo výške XY € + XY %, v prípade platby cez PayPal je to XY € + XY %.
    </p>
  </div>
	<h3>Čo to celé stojí?</h3>
  <div>
    <p>
 V prípade úspešného projektu, si účtujeme províziu vo výške 4% z dosiahnutej sumy a 2,5% za transakčné poplatky. V prípade, že je projekt neúspešný, všetky príspevky sú vrátené prispievateľom v plnej výške.
    </p>
  </div>
	<h3>Ostane môj projekt mojím aj po zverejnení na ASDF?</h3>
  <div>
    <p>
   		Náš portál si za žiadnych okolnosí nenárokuje žiadne práva na Tvoj projek. Môžeš si byť istý, že Tvoj projekt ostane Tvojím či bude úspešný alebo nie.
    </p>
  </div>
	<h3>Aká je šanca na úspech?</h3>
  <div>
    <p>
   		Šanca na úspech je závislá len a len od teba. Môžeš si byť istý, že tvoj projekt budeme propagovať zo všetých síl ale primárne je propagácia v Tvojich rukách. Zaväzujeme sa, že každý zverejnený projekt podporíme aj finančne ako "ľudia z davu", a ak budeš chcieť, radi sa budeme podieľať aj na jeho realizácii.
    </p>
  </div>
